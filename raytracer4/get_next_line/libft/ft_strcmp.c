#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	while ((*s1 || *s2) && *(s1++) == *(s2++))
		(void)0;
	return (*(unsigned char *)(s1 - 1) - *(unsigned char *)(s2 - 1));
}
