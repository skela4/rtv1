#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*s;
	size_t	len;

	len = ft_strlen(s1);
	if (!(s = (char *)malloc(sizeof(*s) * (len + 1))))
		return (NULL);
	return (ft_memcpy(s, s1, len + 1));
}
