#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	char	*f;

	f = ft_strstr(haystack, needle);
	if (!f || (size_t)(f - haystack + ft_strlen(needle)) > len)
		return (NULL);
	return (f);
}
