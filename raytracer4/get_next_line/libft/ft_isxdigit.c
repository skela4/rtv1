#include "libft.h"

int		ft_isxdigit(int c)
{
	if (!ft_isascii(c))
		return (0);
	return ((c >= '0' && c <= '9')
			|| (c >= 'a' && c <= 'f')
			|| (c >= 'A' && c <= 'F'));
}
