#include "libft.h"

int		ft_isspace(int c)
{
	if (!ft_isascii(c))
		return (0);
	return (c == '\t'
			|| c == '\n'
			|| c == '\r'
			|| c == '\v'
			|| c == '\f'
			|| c == ' ');
}
