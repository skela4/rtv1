#include "libft.h"

int		ft_atoi(const char *str)
{
	long	int		sign;
	long	int		nb;

	sign = 1;
	nb = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
		sign = (*(str++) == '-') ? -1 : 1;
	while (*str >= '0' && *str <= '9')
		nb = nb * 10 + *(str++) - '0';
	return (nb * sign);
}
