#include "libft.h"

int		ft_ispunct(int c)
{
	if (!ft_isascii(c))
		return (0);
	return ((c >= (int)041 && c <= (int)047)
			|| (c >= (int)050 && c <= (int)057)
			|| (c >= (int)072 && c <= (int)077)
			|| c == (int)0100
			|| (c >= (int)0133 && c <= (int)0137)
			|| c == (int)0140
			|| (c >= (int)0173 && c <= (int)0176));
}
