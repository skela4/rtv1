#include "libmath.h"

void			mul_mt(t_tuple out, const t_matrix_4 m, const t_tuple t)
{
	t_tuple		mult;

	mult[0] = m[0] * t[0] + m[1] * t[1] + m[2] * t[2] + m[3] * t[3];
	mult[1] = m[4] * t[0] + m[5] * t[1] + m[6] * t[2] + m[7] * t[3];
	mult[2] = m[8] * t[0] + m[9] * t[1] + m[10] * t[2] + m[11] * t[3];
	mult[3] = m[12] * t[0] + m[13] * t[1] + m[14] * t[2] + m[15] * t[3];
	out[0] = mult[0];
	out[1] = mult[1];
	out[2] = mult[2];
	out[3] = mult[3];
}
